        if not docs:
            return "–†–µ–ª–µ–≤–∞–Ω—Ç–Ω–∞—è –∏–Ω—Ñ–æ—Ä–º–∞—Ü–∏—è –≤ –±–∞–∑–µ –∑–Ω–∞–Ω–∏–π –Ω–µ –Ω–∞–π–¥–µ–Ω–∞."
        
        formatted_docs = []
        for i, doc in enumerate(docs, 1):
            formatted_docs.append(f"–î–æ–∫—É–º–µ–Ω—Ç {i}:\n{doc}")
        
        return "\n\n".join(formatted_docs)
    
